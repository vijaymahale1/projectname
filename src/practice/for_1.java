package practice;

public class for_1 {

	public static void main(String[] args) {
		
	//40 to 50
		
		for(int a=40;a<=50;a++) {
		
			if(a%2==0) {
				
				
				System.out.println(a);
			}
			
			
			
			
		}
		
		for(int b=50;b>=40;b--) {
			
			
			if(b%2==1) {
				
				System.out.println(b);
			}
		
		}
	

	}

}
