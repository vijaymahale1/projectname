package Looping;

public class Loop_2 {

	public static void main(String[] args) {
		
		char a='j';
		
		switch(a) {
		 
		case 'v': System.out.println("monday");
		
		case 'i':System.out.println("tuesday");
		break;
		
		case 'j':System.out.println("wensday");
		break;
		
		case 'a':System.out.println("thursday");
		
		case 'y':System.out.println("friday");
		}

	}

}
