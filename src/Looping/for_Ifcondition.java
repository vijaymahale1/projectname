package Looping;

public class for_Ifcondition {

	public static void main(String[] args) {
		
		// even number----------->2,4,6,8,10
		for(int a=1;a<=10;a++) {
			
			if(a%2==0) {
				System.out.println(a);
			}
			
			
			// odd number-------->1,3,5,7
			
			if(a%2==1) {
				System.out.println(a);
			}
				
			}
	}

}
