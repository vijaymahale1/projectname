package a14_string;

public class End_with {

	public static void main(String[] args) {
		// End With anyone string to stop particular char
		//end with command used
		
		
		String a="APPLICATION";
		
	   System.out.println(a.endsWith("ION"));
		
	   System.out.println(a.endsWith("IONP"));
	
		
		// start with anyone string to find first word or char
	   
	   
	   String b="INDIA";
	   
	   System.out.println(b.startsWith("IND"));
	   
	   System.out.println(b.startsWith("AND"));
	}

}
