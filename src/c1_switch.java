
public class c1_switch {

	public static void main(String[] args) {
	
		
		int b=3;
		
		switch(b) {
		
		case 1:System.out.println("vijay");
		
		
		case 2:System.out.println("mahale");
		
		
		case 3:System.out.println("software tester");
		
		
		case 4:System.out.println("ajile sprint");
		
		
		
		default:System.out.println("out");
		}

	}

}
