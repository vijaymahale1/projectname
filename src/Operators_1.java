
public class Operators_1 {

	public static void main(String[] args) {
		int P=40;
		
		// p++ ---->post increment (40)
		// ++p----> pre increment(41)
		
		System.out.println(P++);// 40
		System.out.println(P);//41
		System.out.println(++P); //42
		
		//( P--)--->Post decrement
		//(--P)---->pre decrement
		
		System.out.println(P--);//42
		System.out.println(P);//41
		System.out.println(--P);//40
		

	}

}
