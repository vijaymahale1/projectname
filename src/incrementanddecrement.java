
public class incrementanddecrement {

	public static void main(String[] args) {
		
		int a=50;
		System.out.println(++a);//51
		System.out.println(a);//51
		System.out.println(a++); //51
		System.out.println(a); //52
		
		// a--=52-1=51
		
		System.out.println(a--);//52
		System.out.println(a);//51
		System.out.println(--a);//  50


	}

}
